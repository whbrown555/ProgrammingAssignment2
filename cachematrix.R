##  *********************************************************************************
##  makeCacheMatrix 
## 
##  This function creates a special "matrix" object that can cache its inverse.
## 
##  Assume that the arg, 'x', is an invertible matrix. Return its inverse. 
##  If the inverse has been cached, return the cached value.
##  If the inverse has NOT been cached, invert it using 'solve(x)' 
##  cache the value before returning the inverted matrix.
## 
##  2016-03-10  whbrown555@gmail.com
##  *********************************************************************************
makeCacheMatrix <- function(x = matrix()) {
    m <- NULL
    set <- function(y) {
        x <<- y
        m <<- NULL
    }
    get <- function() x
    setInvMtx <- function(solve) m <<- solve
    getInvMtx <- function() m
    list(set = set, get = get,
         setInvMtx = setInvMtx,
         getInvMtx = getInvMtx)    
}

##  *********************************************************************************
##  cacheSolve 
##  This function computes the inverse of the special "matrix" returned by 
##  makeCacheMatrix above. If the inverse has already been calculated 
##  (and the matrix has not changed), then the cachesolve should retrieve the 
##  inverse from the cache.
## 
##  2016-03-10  whbrown555@gmail.com
##  *********************************************************************************
cacheSolve <- function(x, ...) {
    ## Return a matrix that is the inverse of 'x'
    m <- x$getInvMtx()
    if(!is.null(m)) {
        message("getting cached data")
        return(m)
    }
    data <- x$get()
    m <- solve(data, ...)
    x$setInvMtx(m)
    m
}

